public class PairTester
{
 public static void main(String[] args)
 {
  Pair num = new Pair(-2, 11);
  System.out.println(num.getSum());
  System.out.println(num.getDifference());
  System.out.println(num.getProduct());
  System.out.println(num.getAverage());
  System.out.println(num.getPositiveDistance());
  System.out.println(num.getMaximum());
  System.out.println(num.getMinimum());
 }
}
